package escpos

import "fmt"

func StringToAsciiBytes(x string) []byte {
	out := []byte(nil)
	for _, i := range x {
		if i >= 32 && i <= 126 {
			out = append(out, byte(i))
		} else if a, found := asciiUpper[i]; found {
			out = append(out, a)
		} else {
			panic(fmt.Errorf("ascii not found: %d\n", i))
		}
	}
	return out
}

